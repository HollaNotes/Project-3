DROP TABLE IF EXISTS ca_realestate;

CREATE TABLE ca_realestate (
	index_id NUMERIC,
	id VARCHAR(20),
	stateId NUMERIC,
	countyId NUMERIC,
	cityId NUMERIC,
	country VARCHAR(3),
	datePostedString VARCHAR(10),
	is_bankOwned NUMERIC,
	is_forAuction NUMERIC,
	event VARCHAR(20),
	price NUMERIC,
	pricePerSquareFoot NUMERIC,
	city VARCHAR(30),
	state VARCHAR(2),
	yearBuilt NUMERIC,
	streetAddress VARCHAR(65),
	zipcode NUMERIC,
	longitude NUMERIC,
	latitude NUMERIC,
	hasBadGeocode NUMERIC,
	description VARCHAR(4000),
	currency VARCHAR(3),
	livingArea NUMERIC,
	livingAreaValue NUMERIC,
	lotAreaUnits VARCHAR(10),
	bathrooms NUMERIC,
	bedrooms NUMERIC,
	buildingArea NUMERIC,
	parking NUMERIC,
	garageSpaces NUMERIC,
	hasGarage NUMERIC,
	levels VARCHAR(40),
	pool NUMERIC,
	spa NUMERIC,
	isNewConstruction NUMERIC,
	hasPetsAllowed NUMERIC,
	homeType VARCHAR(15),
	county VARCHAR(25)
);

SELECT * FROM ca_realestate;
